//switch statement

var weight=parseFloat(prompt("enter your weight"));
switch(weight)
{
    case 20:
            document.write("Ammu weight");
            break;
        
    case 40:
            document.write("Charan weight");
            break;

    case 50:
            document.write("Shubha or srinivas weight");
            break;

    default:document.write("weight is not in 20,40,50");

}

